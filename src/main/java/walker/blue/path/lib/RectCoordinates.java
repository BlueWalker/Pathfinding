public class RectCoordinates {
    private int x;
    private int y;
    private int z;

    /**
     * Class constructor.
     */
    public RectCoordinates(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public int x() { return x; }

    public int y() { return y; }

    public int z() { return z; }
}
